    private boolean doLogin(){
        TypedQuery<Login> query = controller.getEntityManager().createNamedQuery("Login.doLogin", Login.class);
        query.setParameter("login", loginField.getText());
        query.setParameter("senha", Utils.getMd5String(senhaField.getText()));
        query.setMaxResults(1);
        List<Login> results = query.getResultList();
        if(results.isEmpty()) return false;
        logged = results.get(0);
        return true;
    }
    
