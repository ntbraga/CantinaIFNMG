    public void preencheTable(){
        caixasTable.clearAll();
        caixas.clear();
        
        caixas.addAll(caixaController.findCaixaEntities());
        for(Caixa caixa: caixas){
            caixasTable
                    .getDefaultTableModel()
                    .addRow(new String[]{caixa.getAberto()?"Sim":"NÃ£o",
                        Utils.DATE_FORMAT.format(caixa.getTimeopen()),
                        caixa.getTimeclose() == null?
                                "-":
                                Utils.DATE_FORMAT.format(caixa.getTimeclose())});
        }
    }
    
    
